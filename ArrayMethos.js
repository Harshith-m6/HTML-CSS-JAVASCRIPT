let numbers = new Array(1,2,3,4,5,6,7,8,9);

numbers.push(11);
console.log(numbers); // 11 is added into the array at the end

numbers.pop() // removes the element from the end
console.log(numbers);


numbers.unshift(30) // adds the element to the first index
console.log(numbers);

numbers.shift() // removes the element from the first index
console.log(numbers);

let number = [1,2,3,4,5,6]
number.fill("*") //fills the array with value
console.log(number);



